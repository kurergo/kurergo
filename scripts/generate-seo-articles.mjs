function frontMatter(article) {
  return `---\ntitle: "${article.title}"\ndate: ${date}\ndescription: "${article.description}"\nkeywords: ${JSON.stringify(article.keywords)}\nfaq: ${JSON.stringify(article.faq.map(([question, answer]) => ({ question, answer })))}\n---\n\n`;
}

function relatedLinks(currentSlug) {
  const links = Object.entries(linkMap)
    .filter(([, url]) => !url.includes(currentSlug))
    .map(([text, url]) => `- [Подробнее о ${text}](${url})`)
    .join("\n");
  const shortLinks = Object.entries(shortLinkMap)
    .map(([text, url]) => `- [Коротко о ${text}](${url})`)
    .join("\n");
  return `\n## Что читать дальше\n\n${links}\n\n### Короткие материалы по теме\n\n${shortLinks}\n`;
}

function faqBlock(faq) {
  return `\n## Вопросы и ответы\n\n${faq
    .map(([q, a]) => `### ${q}\n\n${a}`)
    .join("\n\n")}\n`;
}

function keywordBlock(article) {
  return `\n## Коротко для поиска и Алисы\n\nЕсли коротко, материал отвечает на запросы: ${article.keywords.join(", ")}. Основная мысль: человеку важно не найти одну красивую цифру, а понять условия, ограничения и порядок действий. Кому это полезно: ${article.audience}. Лучший путь: изучить требования, выбрать понятный район, начать с умеренного слота и оценить личный результат после первых выходов.\n`;
}

