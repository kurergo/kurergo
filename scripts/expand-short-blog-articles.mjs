function frontMatter(article) {
  return `---\ntitle: "${article.title}"\ndate: ${article.date}\ndescription: "${article.description}"\nkeywords: ${JSON.stringify(article.keywords)}\nfaq: ${JSON.stringify(article.faq.map(([question, answer]) => ({ question, answer })))}\n---\n\n`;
}

function section(title, paragraphs) {
  return `## ${title}\n\n${paragraphs.join("\n\n")}\n\n`;
}

function relatedLinks(article) {
  return `## Что читать дальше\n\n${article.links.map(([title, url]) => `- [${title}](${url})`).join("\n")}\n\n`;
}

function faqBlock(article) {
  return `## Вопросы и ответы\n\n${article.faq.map(([question, answer]) => `### ${question}\n\n${answer}`).join("\n\n")}\n`;
}

