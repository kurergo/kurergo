function makeArticle(article, index) {
  let body = frontMatter(article);
  body += intro(article);
  body += baseContent(article);
  body += relatedLinks(article, index);

  const extras = extraParagraphs(article);
  let cursor = 0;
  while (body.length < 12200) {
    body += `\n${extras[cursor % extras.length]}\n`;
    cursor += 1;
  }

  body += faqBlock(article);
  return body;
}

