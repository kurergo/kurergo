function relatedLinks(article, index) {
  const sameCluster = topics.filter((item) => item.category === article.category);
  const clusterIndex = sameCluster.findIndex((item) => item.slug === article.slug);
  const same = [-2, -1, 1, 2, 3]
    .map((offset) => sameCluster[(clusterIndex + offset + sameCluster.length) % sameCluster.length])
    .filter((item) => item && item.slug !== article.slug);
  const bridges = (clusterBridges[article.category] ?? [])
    .map((slug) => topics.find((item) => item.slug === slug))
    .filter(Boolean);
  const links = [...same, ...bridges, topics[(index + 1) % 50]].filter(Boolean);
  const seen = new Set();
  const articleLinks = links
    .filter((item) => {
      if (seen.has(item.slug) || item.slug === article.slug) return false;
      seen.add(item.slug);
      return true;
    })
    .slice(0, 6)
    .map((item) => `- [${item.title}](/wiki/${item.slug}/)`)
    .join("\n");
  const baseLinks = [
    clusterHubs[article.category],
    ["Большой разбор дохода курьера", "/wiki/skolko-zarabatyvaet-kurier-yandex-2026/"],
    ["Инструкция по регистрации", "/wiki/rabota-kurierom-yandex-registraciya/"],
    ["Полный список материалов WIKI", "/wiki/"],
  ]
    .filter(Boolean)
    .filter(([, url]) => url !== `/wiki/${article.slug}/`)
    .filter(([title, url], idx, arr) => arr.findIndex(([, otherUrl]) => otherUrl === url) === idx)
    .map(([title, url]) => `- [${title}](${url})`)
    .join("\n");
  return `\n## Что читать дальше\n\n${articleLinks}\n\n### Базовые материалы блога\n\n${baseLinks}\n`;
}

